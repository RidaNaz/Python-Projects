def main():
    # This for-loop starts at 0 and counts up to 19 (for a total of 20 numbers)
    for i in range(20):
        print(i * 2, end=" ")  # Multiply the loop index by 2 to get the even number, print on the same line

# Python boilerplate to call the main function
if __name__ == "__main__":
    main()
